class FourNodeTwoLayerNN:
    def __init__(self, learning_rate, n_features):
        # Layer 1: input -> 2
        self.W1 = np.random.randn(2, n_features) * 0.01
        self.b1 = np.zeros(2)

        # Layer 2: 2 -> 2
        self.W2 = np.random.randn(2, 2) * 0.01
        self.b2 = np.zeros(2)

        # Output: 2 -> 1
        self.W3 = np.random.randn(1, 2) * 0.01
        self.b3 = 0.0

        self.learning_rate = learning_rate

    def _sigmoid(self, x):
        x = np.clip(x, -50, 50)
        return 1.0 / (1.0 + np.exp(-x))

    def _sigmoid_deriv_from_sigmoid(self, s):
        return s * (1.0 - s)

    def _forward_single(self, x):
        z1 = self.W1 @ x + self.b1
        a1 = self._sigmoid(z1)

        z2 = self.W2 @ a1 + self.b2
        a2 = self._sigmoid(z2)

        z3 = self.W3 @ a2 + self.b3
        yhat = self._sigmoid(z3)[0]

        return a1, a2, yhat

    def predict_proba(self, X):
        if hasattr(X, "values"):
            X = X.values
        X = np.asarray(X, dtype=float)

        if X.ndim == 1:
            _, _, yhat = self._forward_single(X)
            return yhat
        else:
            Z1 = X @ self.W1.T + self.b1      # (n,2)
            A1 = self._sigmoid(Z1)

            Z2 = A1 @ self.W2.T + self.b2     # (n,2)
            A2 = self._sigmoid(Z2)

            Z3 = A2 @ self.W3.T + self.b3     # (n,1)
            Yhat = self._sigmoid(Z3).reshape(-1)
            return Yhat

    def predict(self, X, threshold=0.5):
        return (self.predict_proba(X) >= threshold).astype(int)

    def _compute_gradients(self, x, y):
        a1, a2, yhat = self._forward_single(x)

        # Output layer
        dL_dyhat = 2.0 * (yhat - y)
        delta3 = dL_dyhat * (yhat * (1.0 - yhat))            # scalar
        dW3 = delta3 * a2.reshape(1, -1)                     # (1,2)
        db3 = delta3

        # Hidden layer 2
        delta2 = (self.W3.T.reshape(2,) * delta3) * self._sigmoid_deriv_from_sigmoid(a2)  # (2,)
        dW2 = delta2.reshape(-1, 1) @ a1.reshape(1, -1)      # (2,2)
        db2 = delta2

        # Hidden layer 1
        delta1 = (self.W2.T @ delta2) * self._sigmoid_deriv_from_sigmoid(a1)  # (2,)
        dW1 = delta1.reshape(-1, 1) @ x.reshape(1, -1)       # (2,d)
        db1 = delta1

        return dW1, db1, dW2, db2, dW3, db3

    def _update(self, dW1, db1, dW2, db2, dW3, db3):
        lr = self.learning_rate
        self.W1 -= lr * dW1
        self.b1 -= lr * db1
        self.W2 -= lr * dW2
        self.b2 -= lr * db2
        self.W3 -= lr * dW3
        self.b3 -= lr * db3

    def train(self, X, Y, iterations=20000, report_every=200):
        errs = []
        n = len(Y)
        for it in range(iterations):
            i = np.random.randint(n)
            grads = self._compute_gradients(X[i], Y[i])
            self._update(*grads)
            if it % report_every == 0:
                yhat_all = self.predict_proba(X)
                errs.append(np.mean((yhat_all - Y) ** 2))
        return errs

