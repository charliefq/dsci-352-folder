plt.scatter(X[T == 1][:, 0], X[T == 1][:, 1], label="Versicolor")

# Decision boundary
w0, w1, w2 = model.w
x_vals = np.linspace(X[:,0].min(), X[:,0].max(), 100)
y_vals = -(w0 + w1*x_vals) / w2
plt.plot(x_vals, y_vals)

